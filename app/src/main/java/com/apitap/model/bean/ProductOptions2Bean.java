public class ProductOptions2Bean {
    private String name_option;
    private String option_id;
    private String choice_id;
    private String choice_name;
    private String choice_price;



    public ProductOptions2Bean() {

    }

    public String getName_option() {
        return name_option;
    }

    public void setName_option(String name_option) {
        this.name_option = name_option;
    }

    public String getOption_id() {
        return option_id;
    }

    public void setOption_id(String option_id) {
        this.option_id = option_id;
    }



    public String getChoice_id() {
        return choice_id;
    }

    public void setChoice_id(String choice_id) {
        this.choice_id = choice_id;
    }



    public String getChoice_name() {
        return choice_name;
    }

    public void setChoice_name(String choice_name) {
        this.choice_name = choice_name;
    }


    public String getChoice_price() {
        return choice_price;
    }

    public void setChoice_price(String choice_price) {
        this.choice_price = choice_price;
    }



}
